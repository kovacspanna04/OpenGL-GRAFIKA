        public unsafe static ModelObjectDescriptor CreateCubeWithFaceColors(GL Gl, Dictionary<int, (float r, float g, float b)> faceColors)
        {
            ModelObjectDescriptor obj = CreateCube(Gl);         // uj kockaobjektum
            float[] colorArray = new float[24 * 4];     // a kocka osszes pontja egy tombbe(r,g,b,a komponensekkel)(a = atlatszosag)
            // 6 oldal, 4 csucs per oldal
            // oldal indexek: 0-tol 5-ig (minden oldalhoz 4 index(pont) tartozik)

            for (int face = 0; face < 6; face++)    // vegigmegyek a 6 oldalon
            {
                // ha meg van adva mar szin ehhez az oldalhoz, akkor azt hasznalom, ha nem akkor fekete lesz
                (float r, float g, float b) color = faceColors.ContainsKey(face) ? faceColors[face] : (0f, 0f, 0f);

                for (int i = 0; i < 4; i++)     // vegigmegyek az adott oldal 4 pontjan
                {
                    int vertexIndex = face * 4 + i;     // megadja az adott oldalhoz tartozo 4 cucs poziciojat
                    colorArray[vertexIndex * 4 + 0] = color.r;
                    colorArray[vertexIndex * 4 + 1] = color.g;
                    colorArray[vertexIndex * 4 + 2] = color.b;
                    colorArray[vertexIndex * 4 + 3] = 1f;       // egyaltalan nem atlatszo
                }
            }

            // uj szin buffert hozok letre
            uint colors = Gl.GenBuffer();
            Gl.BindVertexArray(obj.Vao);        // hozzakotom az aktualis vao-hoz

            Gl.BindBuffer(GLEnum.ArrayBuffer, colors);
            Gl.BufferData(GLEnum.ArrayBuffer, (ReadOnlySpan<float>)colorArray.AsSpan(), GLEnum.StaticDraw);
            Gl.VertexAttribPointer(1, 4, VertexAttribPointerType.Float, false, 0, null);        // az 1-es attributumhelyre(szin) kerulnek ezek az ertekek(minden csucshoz van 4 komponens)
            Gl.EnableVertexAttribArray(1);
            Gl.BindBuffer(GLEnum.ArrayBuffer, 0);

            obj.Colors = colors;
            return obj;
        }

